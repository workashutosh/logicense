function resolveIcon(slug) {
    if (!slug) return null;
    const pascal = slug
        .split(/[-_]/)
        .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
        .join("");
    const key = `si${pascal}`;
    if (simpleIcons[key]) return simpleIcons[key];

    const found = Object.values(simpleIcons).find(
        (icon) => icon && typeof icon === "object" && icon.slug === slug
    );
    if (found) return found;
    if (FALLBACKS[slug]) return { slug, ...FALLBACKS[slug] };
    return null;
}

export function getBrand(slug) {
    return resolveIcon(slug);
}

export default function BrandIcon({
    slug,
    title,
    className = "h-5 w-5",
    colored = false,
    label = true,
}) {
    const icon = resolveIcon(slug);
    if (!icon) return null;

    const fill = colored ? `#${icon.hex}` : "currentColor";

    return (
        <svg
            role="img"
            viewBox="0 0 24 24"
            xmlns="http://www.w3.org/2000/svg"
            className={className}
            aria-label={label ? title || icon.title : undefined}
            aria-hidden={label ? undefined : true}
        >
            {label ? <title>{title || icon.title}</title> : null}
            <path d={icon.path} fill={fill} />
        </svg>
    );
}
